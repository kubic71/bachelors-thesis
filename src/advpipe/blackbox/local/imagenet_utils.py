# Precompute animate/inanimate category split  
def _create_organism_category_split():
    import os
    from advpipe import utils
    from advpipe.language_model.label_classification import OrganismLabelClassifier
    classifier = OrganismLabelClassifier()

    is_organism_dict = {}
    for idx in range(1000):
        is_organism = classifier.is_organism_synset(idx_to_synset[idx])
        print(f"{idx}\t{idx_to_synset[idx]}\t{'organism' if is_organism else 'object'}\t{idx_to_label[idx]}")
        is_organism_dict[idx] = is_organism

    with open(os.path.join(utils.rel_to_abs_path("."), "imagenet_label_split.py"), "w") as f:
        f.write(str(is_organism_dict))

    return is_organism_dict
